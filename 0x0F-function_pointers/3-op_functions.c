#include "3-calc.h"
/**
 * @op_add - addtwo num
 * @a: first numbeer
 * @b: sec number
 * Return: return sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract two numbers
 * @a: first number
 * @b: second number
 * Return: returns subtraction of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two numbers
 * @a: first number
 * @b: second number
 * Return: returns multiplication of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - returns division of two numbers
 * @a: first number
 * @b: second number
 * Return: returns division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns modulas of a num
 * @a: first num
 * @b: sec  num
 * Return: return modulus of and b 
 */
int op_mod(int a, int b)
{
	return (a % b);
}
